                      console.log("Adult count changed:", this.value);
                      // Trigger room search and price calculation
                      if (
                        typeof GmzBookingCustom.initHotelRoomSearch ===
                        "function"
                      ) {
                        GmzBookingCustom.initHotelRoomSearch();
                      }
                    });
                  }

                  if (childrenSelect) {
                    childrenSelect.addEventListener("change", function () {
                      console.log("Children count changed:", this.value);
                      // Trigger room search and price calculation
                      if (
                        typeof GmzBookingCustom.initHotelRoomSearch ===
                        "function"
                      ) {
                        GmzBookingCustom.initHotelRoomSearch();
                      }
                    });
                  }

                  // Handle extra services changes
                  var extraCheckboxes =
                    form.querySelectorAll(".gmz-checkbox-item");
                  extraCheckboxes.forEach(function (checkbox) {
                    checkbox.addEventListener("change", function () {
                      console.log(
                        "Extra service changed:",
                        this.value,
                        this.checked
                      );
                      // Trigger price calculation
                      if (
                        typeof GmzBookingCustom.initHotelPriceCalculation ===
                        "function"
                      ) {
                        GmzBookingCustom.initHotelPriceCalculation();
                      }
                    });
                  });

