                // Add additional event listeners for better price calculation
                var form = document.querySelector(
                  ".booking-form-single.hotel"
                );
                if (form) {
                  console.log(
                    "Hotel booking form found, setting up event listeners..."
                  );

                  // Handle room selection changes more robustly
                  form.addEventListener("change", function (e) {
                    if (e.target.name && e.target.name.startsWith("room[")) {
                      console.log(
                        "Room selection changed:",
                        e.target.name,
                        e.target.value
                      );
                      // Trigger price calculation after a short delay to ensure form data is updated
                      setTimeout(function () {
                        if (
                          typeof GmzBookingCustom.initHotelPriceCalculation ===
                          "function"
